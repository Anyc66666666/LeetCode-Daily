func wordBreak(s string, wordDict []string) bool {
	// è£…æ»¡èƒŒåŒ…sçš„å‰å‡ ä½å­—ç¬¦çš„æ–¹å¼æœ‰å‡ ç§
	dp := make([]int, len(s)+1)
	dp[0] = 1
	for i := 0; i <= len(s); i++ {
		for j := 0; j < len(wordDict); j++ {
			if i >= len(wordDict[j]) &&
				wordDict[j] == s[i-len(wordDict[j]):i] {
				dp[i] += dp[i-len(wordDict[j])]
			}
		}
	}
	return dp[len(s)] >= 0
}

