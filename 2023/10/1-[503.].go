func nextGreaterElements(nums []int) []int {
	var stack []int
	length := len(nums)
	ans := make([]int, length)
	for i := range ans {
		ans[i] = -1
	}

	for i := 0; i < 2*length-1; i++ {
		for len(stack) > 0 && nums[i%length] > nums[stack[len(stack)-1]] {
			ans[stack[len(stack)-1]] = nums[i%length]
			stack = stack[:len(stack)-1]
		}
		stack = append(stack, i%length)
	}
	return ans

}

