func removeDuplicates(nums []int) int {
	m := make(map[int]int, 0)

	nums = sort(nums)
	for _, v := range nums {
		m[v] = v
	}
	i := 0
	var s []int
	for _, v := range m {
		s = append(s, v)
	}
	sort(s)
	for _, v := range s {
		nums[i] = v
		i++
	}

	nums = nums[:i]

	return i

}

func sort(nums []int) []int {
	for i := 0; i < len(nums)-1; i++ {
		for j := i + 1; j < len(nums); j++ {
			if nums[i] > nums[j] {
				nums[j], nums[i] = nums[i], nums[j]
			}
		}
	}
	return nums
}

